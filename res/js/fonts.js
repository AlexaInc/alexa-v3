// =====================================================
// MAIN CONVERTER FUNCTION
// =====================================================

function convert(text, font) {
  text = String(text || ''); // force string, fallback to empty string
  let out = "";
  for (const ch of text.toLowerCase()) {
    out += font[ch] || ch; // keep original if no match
  }
  return out;
}


